'''string  ==> It is sequence of characters.
               immutable in nature (can't be change further)
               - we can write double quote in single quote
               - single quote in double
               - triple quote for multi-line string
'''

a = "Vibhu"
print(a)
print(type(a))

b = "My name is 'vibhu'!!"
print(b)
print(type(b))

c = 'My name is "vibhu"!!'
print(c)
print(type(c))

d = '''My name 
        is vibhu'''
print(d)
print(type(d))