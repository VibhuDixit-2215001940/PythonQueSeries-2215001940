'''CHANGING OF CASE OF STRING
------------------------------
upper() --> covert to all alphabet to upper case
title() --> covert to first alphabet of every word to upper case rest to lower case
lower() --> covert to all alphabet to lower case
capitalize() --> covert only first alphabet to upper case rest to lower case'''

x = "mY name Is vibHu dixit"
print(x.upper())
print(x.title())
print(x.lower())
print(x.capitalize())